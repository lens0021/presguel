/// 조합용 자모 코드포인트의 날개셋 서열번호. 표에 없으면 None.
pub fn ngs_seq(cat: Category, cp: u32) -> Option<u32> {
    let tbl: &[(u32, u32)] = match cat {
        Category::Cho => CHO_SEQ,
        Category::Jung => JUNG_SEQ,
        Category::Jong => JONG_SEQ,
    };
    tbl.binary_search_by_key(&cp, |&(c, _)| c)
        .ok()
        .map(|i| tbl[i].1)
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn known_seqs() {
        // 사용자 ㅋㅋㅋ/ㅎㅎㅎ 규칙의 핵심 상수.
        assert_eq!(ngs_seq(Category::Cho, 0x110F), Some(176)); // ㅋ 초성
        assert_eq!(ngs_seq(Category::Cho, 0x1112), Some(185)); // ㅎ 초성
        assert_eq!(ngs_seq(Category::Cho, 0x1100), Some(1)); // ㄱ 초성
        assert_eq!(ngs_seq(Category::Jong, 0x11A8), Some(1)); // ㄱ 종성
        assert_eq!(ngs_seq(Category::Jung, 0x1161), Some(1)); // ㅏ
        assert_eq!(ngs_seq(Category::Jung, 0x1169), Some(21)); // ㅗ
        assert_eq!(ngs_seq(Category::Jung, 0x116E), Some(43)); // ㅜ
        assert_eq!(ngs_seq(Category::Jung, 0x1173), Some(64)); // ㅡ
    }

    #[test]
    fn tables_sorted_unique() {
        for tbl in [CHO_SEQ, JUNG_SEQ, JONG_SEQ] {
            assert!(tbl.windows(2).all(|w| w[0].0 < w[1].0));
        }
    }

    #[test]
    fn unknown_is_none() {
        assert_eq!(ngs_seq(Category::Cho, 0x0041), None);
    }
}
